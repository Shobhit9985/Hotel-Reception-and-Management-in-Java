
import java.time.Year;

public class Building {
    private int builtYear;
    private int numberOfFloors;

    public Building() {
        builtYear = 2022;
        numberOfFloors = 1;
    }

    public Building(int builtYear, int numberOfFloors) {
        this.builtYear = builtYear;
        this.numberOfFloors = numberOfFloors;
    }

}